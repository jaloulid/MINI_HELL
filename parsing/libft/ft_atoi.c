#include "libft.h"

int	ft_atoi(const char *str)
{
	int				sign;
	unsigned long	result;
	int				i;

	i = 0;
	result = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		result = result * 10 + (str[i] - 48);
		i++;
		if (result >= LONG_MAX && sign == -1)
			result = 0;
		else if (result >= LONG_MAX && sign == 1)
			result = -1;
	}
	return (result * sign);
}
